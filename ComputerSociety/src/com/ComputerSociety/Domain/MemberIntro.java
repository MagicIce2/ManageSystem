package com.ComputerSociety.Domain;

/**
 * 成员简介部分数据属性
 */

public class MemberIntro {

    private int staff_id;
    private String staff_picture;
    private String staff_name;
    private String staff_depart;
    private String staff_profession;
    private String staff_class;
    private String staff_duty;
    private String staff_introduce;
    private String staff_motto;

    public int getStaff_id() {
        return staff_id;
    }

    public void setStaff_id(int staff_id) {
        this.staff_id = staff_id;
    }

    public String getStaff_picture() {
        return staff_picture;
    }

    public void setStaff_picture(String staff_picture) {
        this.staff_picture = staff_picture;
    }

    public String getStaff_name() {
        return staff_name;
    }

    public void setStaff_name(String staff_name) {
        this.staff_name = staff_name;
    }

    public String getStaff_depart() {
        return staff_depart;
    }

    public void setStaff_depart(String staff_depart) {
        this.staff_depart = staff_depart;
    }

    public String getStaff_profession() {
        return staff_profession;
    }

    public void setStaff_profession(String staff_profession) {
        this.staff_profession = staff_profession;
    }

    public String getStaff_class() {
        return staff_class;
    }

    public void setStaff_class(String staff_class) {
        this.staff_class = staff_class;
    }

    public String getStaff_duty() {
        return staff_duty;
    }

    public void setStaff_duty(String staff_duty) {
        this.staff_duty = staff_duty;
    }

    public String getStaff_introduce() {
        return staff_introduce;
    }

    public void setStaff_introduce(String staff_introduce) {
        this.staff_introduce = staff_introduce;
    }

    public String getStaff_motto() {
        return staff_motto;
    }

    public void setStaff_motto(String staff_motto) {
        this.staff_motto = staff_motto;
    }
}
